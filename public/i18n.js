// Aktuellt språk – sparas mellan besök.
function getLang() {
  const saved = localStorage.getItem('brevklar_lang');
  if (saved && I18N[saved]) return saved;
  const browser = (navigator.language || 'sv').slice(0, 2);
  return I18N[browser] ? browser : 'sv';
}

function t(key) {
  const lang = getLang();
  return (I18N[lang] && I18N[lang][key]) || I18N.sv[key] || key;
}

function setLang(lang) {
  if (!I18N[lang]) return;
  localStorage.setItem('brevklar_lang', lang);
  applyTranslations();
}

function applyTranslations() {
  const lang = getLang();
  const dir = I18N[lang]._dir;
  document.documentElement.lang = lang;
  document.documentElement.dir = dir;

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-ph]').forEach((el) => {
    el.setAttribute('placeholder', t(el.getAttribute('data-i18n-ph')));
  });

  const picker = document.getElementById('lang-picker');
  if (picker) picker.value = lang;

  // Låt appen rerendra ev. resultat på nytt språk.
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}
